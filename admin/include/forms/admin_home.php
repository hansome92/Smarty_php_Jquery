<p><font size="+1">How to configure a DaDaBIK application</font></p>
<p>Some general configuration parameters can be directly set from the file <b>/include/config.php</b>, just by opening it with a plain text editor and editing it. The file is self-explanatory. At that level you can, for example, set the database you want to manage with DaDaBIK or enable/disable/configure some DaDaBIK features (e.g. authentication, upload, email notices, language,...).</p>
<p>For a more analytic tuning you are supposed to use this admin area. At this level you can:

<ul>
<li>Choose the database tables you want to include in your DaDaBIK application</li>
<li>Configure your datagrids</li>
<li>Configure your forms</li>
<li>Set the permissions</li>
<li>Synchronize your applications if you change the database schema</li>
</ul>
</p>

